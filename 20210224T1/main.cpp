using namespace std;

inline long long read()
{
	long long x = 0;
	int f = 1;
	char ch = getchar();
	while (ch < '0' || ch>'9')
	{
		if (ch == '-')
			f = -1;
		ch = getchar();
	}
	while (ch >= '0' && ch <= '9')
	{
		x = (x << 1) + (x << 3) + (ch ^ 48);
		ch = getchar();
	}
	return x * f;
}
void write(const long long& x)
{
	if (!x)
	{
		putchar('0');
		return;
	}
	char f[100];
	long long tmp = x;
	if (tmp < 0)
	{
		tmp = -tmp;
		putchar('-');
	}
	long long s = 0;
	while (tmp > 0)
	{
		f[s++] = tmp % 10 + '0';
		tmp /= 10;
	}
	while (s > 0)
	{
		putchar(f[--s]);
	}
}

long long totN;
long long A[500090];

int main()
{
	freopen("thunder.in", "r", stdin);
	freopen("thunder.out", "w", stdout);
	totN = read();
	for (int i = 1; i <= totN; ++i)
	{
		A[i] = read();
	}
	for (register int i = 1; i <= totN; ++i)
	{
		long long nowANS = -1;
		for (register int j = 1; j <= totN; ++j)
		{
			nowANS = max(nowANS, A[j] - A[i] + (long long )ceil(sqrt((double) abs(i - j))));
		}
		write(nowANS);
		putchar('\n');
	}
	return 0;
}//Thomitics Code
